//3. Loop Challenge

// Using 'for' loop
console.log("For Loop:");
for (let i = 1; i <= 10; i++) {
    console.log(i);
}

// Using 'while' loop
console.log("While Loop:");
let j = 1;
while (j <= 10) {
    console.log(j);
    j++;
}

// Using 'do...while' loop
console.log("Do-While Loop:");
let k = 1;
do {
    console.log(k);
    k++;
} while (k <= 10);