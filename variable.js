let a = 10;
const b = 20;
var c = 30;

// Reassigning values
a = 15;  // Works because 'let' allows reassignment
// b = 25; // This will cause an error because 'const' cannot be reassigned
c = 35;  // Works because 'var' allows reassignment

console.log(a, b, c);

// Observations:

// let allows reassignment.

// const does not allow reassignment (it throws an error).

// var allows reassignment but is function-scoped, unlike let (block-scoped).


